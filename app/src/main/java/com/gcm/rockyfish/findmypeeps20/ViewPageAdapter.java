public class ViewPageAdapter extends FragmentStatePagerAdapter {

    CharSequence Titles[]; // This will Store the Titles of the Tabs which are Going to be passed when ViewPagerAdapter is created
    int NumbOfTabs; // Store the number of tabs, this will also be passed when the ViewPagerAdapter is created


    // Build a Constructor and assign the passed Values to appropriate values in the class
    public ViewPageAdapter(FragmentManager fm, CharSequence mTitles[], int mNumbOfTabsumb) {
        super(fm);

        this.Titles = mTitles;
        this.NumbOfTabs = mNumbOfTabsumb;

    }

    //This method return the fragment for the every position in the View Pager
    @Override
    public Fragment getItem(int position) {

        if(position == 0) // if the position is 0 we are returning the First tab
        {
            Map_Fragment map_tab = new Map_Fragment();
            return map_tab;
        }
        else if(position == 1)
        {// As we are having 2 tabs if the position is now 0 it must be 1 so we are returning second tab
            Friend_Fragment friend_tab = new Friend_Fragment();
            return friend_tab;
        }
        else if(position == 2)
        {
            Pending_Fragment pending_tab = new Pending_Fragment();

            return pending_tab;
        }
        else
        {
            Status_Fragment status_tab = new Status_Fragment();
            return status_tab;
        }

    }

    // This method return the titles for the Tabs in the Tab Strip

    @Override
    public CharSequence getPageTitle(int position) {
        return Titles[position];
    }

    // This method return the Number of tabs for the tabs Strip

    @Override
    public int getCount() {
        return NumbOfTabs;
    }

}
